course = "Python for Beginners"

print(len(course)) #length of the string
print(course[0]) #first character
print(course[-1]) #last character
print(course[0:6]) #substring from index 0 to 5 
print(course[:]) #substring from index 0 to the end of the string
print(course[:3]) #substring from index 0 to 2
print(course[3:]) #substring from index 3 to the end of the string
